    public V_Usuario(String operacion) {
        initComponents();
        
        if (operacion == "modificar") 
            tfUsuario.setEditable(false);
            tfContraseña.setEditable(false);
            bAceptar.setEditable(false);
            
        if (operacion == "baja") 
         
            tfUsuario.setEditable(false);
            tfContraseña.setEditable(false);
            bAceptar.setEditable(false);
        if (operacion == "alta")   
            tfCodigoUsuario.setVisible(false);
            bAceptar.setEditable(false);
    }
