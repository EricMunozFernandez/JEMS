    private void bMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bMostrarActionPerformed
        if(comprobarCampoVacio() && comprobarRBSeleccionado()){
            if(rbDueño.isSelected()){
                ControladorVistas.mostrarVentanaMostrarDueño();
                ControladorVistas.cerrarVentanaMostrarDueño();
            } else if(rbEquipo.isSelected()) {
                ControladorVistas.mostrarVentanaMostrarEquipo();
                ControladorVistas.cerrarVentanaMostrarEquipo();
            } else if(rbJugador.isSelected()) {
                ControladorVistas.mostrarVentanaMostrarJugador();
                ControladorVistas.cerrarVentanaMostrarJugador();
            }
        }
    }//GEN-LAST:event_bMostrarActionPerformed

