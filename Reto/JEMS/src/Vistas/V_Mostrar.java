    public boolean comprobarCampoVacio(){
        boolean flag = true;
        if(tfCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Introduce el codigo");
            flag = false;
        }        
        return flag;
    }
    
    public boolean comprobarRBSeleccionado(){
        boolean flag = true;
        if(!rbDueño.isSelected() && !rbEquipo.isSelected() && !rbJugador.isSelected()) {
            JOptionPane.showMessageDialog(rootPane, "Selecciona una opcion");
            flag = false;
        }
        return flag;
    }

    public static int getCodigo(){
        int codigo = tfCodigo.getText();
        return codigo;
    }
    
